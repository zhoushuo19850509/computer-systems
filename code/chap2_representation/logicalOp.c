#include <stdio.h>


/**
 * 这个代码展示非、与、或的操作 ! && ||
 * 我们要特别注意和上一小节位操作(取反、按位与、按位或 ~ & |)进行区别
 * 1.位操作(取反、按位与、按位或 ~ & |)是针对bit进行操作
 * 2.非、与、或的操作 ! && || 的意思完全不一样
 * 
*/
int main(){
    printf("hello ...\n");
    printf("%.2x \n", 0x46);
    printf("%.2x \n", !0x41);
    printf("%.2x \n", !0x00);
    printf("%.2x \n", !!0x01);
    printf("%.2x \n", (0x69 && 0x55));
    printf("%.2x \n", (0x69 || 0x55));
    
    
}